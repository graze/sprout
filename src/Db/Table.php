namespace Graze\Sprout\Db;

class Table
{
    /** @var string */
    private $name;
    /** @var string */
    private $path;

    /**
     * ParsedTable constructor.
     *
     * @param string $name
     * @param string $path Optional path
     */
    public function __construct(string $name, string $path = '')
    {
        $this->name = $name;
        $this->path = $path;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @return string
     */
    public function getPath(): string
    {
        return $this->path;
    }
}
